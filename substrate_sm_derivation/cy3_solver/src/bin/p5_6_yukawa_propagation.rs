//! P5.6 — empirical σ_MA → Yukawa-eigenvalue propagation.
//!
//! Closes the brutal-review §4.2 concern: the linear-in-σ propagation
//! claim used by the σ-budget calculation was hand-wavy. This binary
//! measures it directly by sweeping the Donaldson optimisation budget,
//! recording (σ, Yukawa-eigenvalues) pairs, and fitting
//! `relative_bias ≈ C · σ^p` (log-log).
//!
//! ## Geometry pivot
//!
//! The task brief specified the Fermat quintic at k=2. However, this
//! codebase has **no Yukawa pipeline on the Fermat quintic**. The
//! Yukawa pipelines (`yukawa_overlap::compute_yukawa_spectrum` and the
//! publication-grade `route34::yukawa_pipeline::predict_fermion_masses`)
//! both operate on the **bicubic Tian-Yau CY3** (CP^3 × CP^3). The
//! Fermat quintic has a σ optimisation workspace (`QuinticSolver`) but
//! no Yukawa coupling pipeline. Building a Fermat-quintic Yukawa
//! pipeline from scratch is well outside this task's scope.
//!
//! So we measure on Tian-Yau where **both** σ_MA and Yukawa are
//! computable. The σ ladder is generated by varying `donaldson_iters`
//! in `solve_ty_metric`. The "Yukawa eigenvalues" we track are the 9
//! fermion masses at M_Z (3 up + 3 down + 3 lepton) returned by
//! `predict_fermion_masses` — these are the diagonalised SVD singular
//! values of `Y_u`, `Y_d`, `Y_e` rescaled by `v / √2` and run to M_Z,
//! which is precisely the quantity the chi-squared discrimination
//! actually scores against PDG.
//!
//! ## Method
//!
//! 1. For each Donaldson budget `(max_iter_i, donaldson_tol_i)`:
//!    a. Run `solve_ty_metric` → record `σ_i`.
//!    b. Wrap the result via `Cy3MetricResultBackground::from_ty`.
//!    c. Run `predict_fermion_masses` → record 9 fermion masses
//!       `λ_i = (m_u, m_c, m_t, m_d, m_s, m_b, m_e, m_mu, m_tau)`.
//! 2. Take the lowest-σ endpoint as empirical "ground truth"
//!    `λ_truth = λ at min σ`.
//! 3. For each other endpoint:
//!      `δλ_i,k = λ_i,k − λ_truth,k`
//!      `rel_i,k = |δλ_i,k| / |λ_truth,k|`
//!      Aggregate as max and mean over k.
//! 4. Fit `log(mean_rel) = log(C) + p · log(σ)` via least-squares.
//!    Report (C, p) and the 5σ-budget recalculation:
//!      `σ_target = (smallest_PDG_relative_uncertainty) / C   (assuming p ≈ 1)`
//!
//! Same seed (42), same n_sample throughout. σ logic is **not**
//! modified. We only vary the optimisation budget.
//!
//! Output:
//!   * Stdout: per-endpoint table + (C, p) fit + budget recalc.
//!   * JSON file at `--output`
//!     (default `output/p5_6_yukawa_propagation.json`).

