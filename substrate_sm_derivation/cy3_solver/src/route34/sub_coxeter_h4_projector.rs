#[cfg(test)]
mod tests {
    use super::*;

    /// Schoen H_4-projection survives ~1/45 of basis at large degree
    /// (1/9 from Z/3 × Z/3 times 1/5 from icosa-Z/5, modulo finite-
    /// degree edge effects).
    #[test]
    fn schoen_h4_projection_survival_at_d4() {
        let basis = build_test_basis(4);
        let projected = build_h4_projected_basis_schoen(&basis);
        let frac = projected.len() as f64 / basis.len() as f64;
        // Expect 1/45 ≈ 0.022. Allow [0.005, 0.15] given small-degree
        // edge effects.
        assert!(
            (0.005..=0.15).contains(&frac),
            "schoen H_4 projection survival {frac} outside [0.005, 0.15] at d=4 \
             (basis_dim {}, projected {})",
            basis.len(),
            projected.len()
        );
        for m in &projected {
            assert_eq!(alpha_character(&m.exponents), 0);
            assert_eq!(beta_character(&m.exponents), 0);
            assert_eq!(icosa_z5_character(&m.exponents), 0);
        }
    }

    /// Idempotency: reapplying the projector is a no-op.
    #[test]
    fn projector_idempotent_h4_schoen() {
        let basis = build_test_basis(3);
        let p1 = build_h4_projected_basis_schoen(&basis);
        let p2 = build_h4_projected_basis_schoen(&p1);
        assert_eq!(p1.len(), p2.len());
        for (a, b) in p1.iter().zip(p2.iter()) {
            assert_eq!(a.exponents, b.exponents);
        }
    }

    /// `χ_5` and `α` characters disagree on a generic monomial: the
    /// fivefold projection is independent of the threefold.
    #[test]
    fn icosa_z5_character_distinct_from_alpha() {
        // x_1^2 has α = 2 (mod 3) and χ_5 = 2 (mod 5); these happen
        // to coincide as integers but the moduli differ.
        let exps = [0u32, 2, 0, 0, 0, 0, 0, 0];
        assert_eq!(alpha_character(&exps), 2);
        assert_eq!(icosa_z5_character(&exps), 2);
        // x_1^3 has α = 0 (Γ-invariant) but χ_5 = 3 (NOT H_4-invariant).
        let exps = [0u32, 3, 0, 0, 0, 0, 0, 0];
        assert_eq!(alpha_character(&exps), 0);
        assert_eq!(icosa_z5_character(&exps), 3);
        // x_1^5 has α = 2 (NOT Γ-invariant) but χ_5 = 0 (Z/5-invariant).
        let exps = [0u32, 5, 0, 0, 0, 0, 0, 0];
        assert_eq!(alpha_character(&exps), 2);
        assert_eq!(icosa_z5_character(&exps), 0);
        // x_1^15 has both α = 0 and χ_5 = 0 — fully H_4-invariant.
        let exps = [0u32, 15, 0, 0, 0, 0, 0, 0];
        assert_eq!(alpha_character(&exps), 0);
        assert_eq!(icosa_z5_character(&exps), 0);
    }
}
