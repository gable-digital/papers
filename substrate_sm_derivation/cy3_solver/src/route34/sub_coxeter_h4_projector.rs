/// Icosahedral fivefold-rotation character on Schoen monomials.
///
/// `χ_5(m) = (a_1 + 2 a_2 + b_1 + 2 b_2) mod 5`, with the same
/// `(0, 1, 2)` weight pattern on each `CP^2` block as the Z/3
/// α-character but reduced mod 5 (the Klein-Tonelli embedding of
/// `2I ⊂ SU(2)` into `(CP^2)²`).
///
/// Reference: Klein 1884 *Vorlesungen über das Ikosaeder* §I.7;
/// Du Val 1964 *Homographies, Quaternions, and Rotations* §8.
#[inline]
pub fn icosa_z5_character(exps: &[u32; 8]) -> u32 {
    (exps[1] + 2 * exps[2] + exps[4] + 2 * exps[5]) % 5
}

/// H_4-projected (Schoen) basis: monomials with `α = 0`, `β = 0`,
/// and `χ_5 = 0` simultaneously.
///
/// This is the gateway sector — the test-function subspace where the
/// journal §L.1 / §L.2 places the H_4 lepton ground state ω_fix.
pub fn build_h4_projected_basis_schoen(basis: &[TestMonomial]) -> Vec<TestMonomial> {
    basis
        .iter()
        .filter(|m| {
            alpha_character(&m.exponents) == 0
                && beta_character(&m.exponents) == 0
                && icosa_z5_character(&m.exponents) == 0
        })
        .cloned()
        .collect()
}

/// H_4-projected (Tian-Yau) basis: monomials with TY/Z3 α = 0 and
/// `χ_5 = 0`. The `χ_5` formula on the TY ambient
/// `[z_0, z_1, z_2, z_3, w_0, w_1, w_2, w_3]` is
/// `(z[1] + 2 z[2] + w[1] + 2 w[2]) mod 5`, the Z/5 partner of the
/// TY/Z3 α-character.
#[inline]
pub fn icosa_z5_character_ty(exps: &[u32; 8]) -> u32 {
    (exps[1] + 2 * exps[2] + exps[5] + 2 * exps[6]) % 5
}

pub fn build_h4_projected_basis_ty(basis: &[TestMonomial]) -> Vec<TestMonomial> {
    basis
        .iter()
        .filter(|m| ty_z3_character(&m.exponents) == 0 && icosa_z5_character_ty(&m.exponents) == 0)
        .cloned()
        .collect()
}

