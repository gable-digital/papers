//! P7.5 — H_4 (icosahedral) sub-Coxeter projector for the gateway test.
//!
//! Companion to [`crate::route34::z3xz3_projector`] and
//! [`crate::route34::metric_laplacian_projected`]. Where those modules
//! project a bigraded test-function basis onto the trivial irrep of the
//! discrete Z/3 (Tian-Yau) or Z/3 × Z/3 (Schoen) symmetry, this module
//! adds an *additional* sub-Coxeter projection onto a subspace
//! identified with the **H_4 (icosahedral) Coxeter sub-group of E_8**.
//!
//! ## Why
//!
//! The journal §L.1 / §L.2 identifies the gateway eigenvalue ω_fix =
//! 1 − 1/dim(E_8) = 123/248 not with the lowest mode of the *full*
//! metric Laplacian, but with the lowest mode within the H_4 lepton
//! sector of E_8. The full bigraded-monomial basis used in
//! [`crate::route34::metric_laplacian`] mixes all sub-Coxeter sectors
//! (H_4 leptons + D_8 quarks + the other E_8 root strata), and P7.1 /
//! P7.1b / P7.2b found no eigenvalue near ω_fix in the resulting
//! spectrum. Hypothesis (3) of the falsification triangle: the basis
//! is the wrong rep — restrict to H_4 first, *then* solve.
//!
//! ## Group-theoretic background (Slansky 1981; Coxeter 1973;
//!    Conway-Sloane 1999 §4)
//!
//! E_8 has Coxeter number `h = 30` and exponents
//! `{1, 7, 11, 13, 17, 19, 23, 29}` — the eight integers in
//! `{1, …, 29}` coprime to 30. These are exactly the exponents of the
//! H_4 (icosahedral) Coxeter group, which is a Coxeter sub-group of
//! E_8 (this is the classical "icosian" embedding of Wilson 2009;
//! Conway-Sloane §4 §8.2). On the McKay-graph side of the (2, 3, 5)
//! triangle, the binary icosahedral group `2I` (order 120) maps to
//! the affine `Ê_8` Dynkin graph; on the Coxeter side, the H_4
//! Coxeter graph is the 4-node `5—3—3` chain whose Weyl group has
//! order 14400 = `120²`.
//!
//! The minimal **non-trivial** unitary representation of the
//! icosahedral rotation group `A_5` (order 60) on the bicubic
//! coordinates `(x_0:x_1:x_2)` (or `(y_0:y_1:y_2)`) is generated by
//! the diagonal fivefold rotation
//!
//! ```text
//!     R_5 : (x_0, x_1, x_2)  →  (x_0, ζ_5 x_1, ζ_5² x_2),
//! ```
//!
//! where `ζ_5 = exp(2π i / 5)`. This is consistent with the standard
//! Klein-Tonelli embedding of the dodecahedron into `CP^2` via
//! homogeneous coordinates with `Z_5`-weighted phases. (Klein 1884
//! "Vorlesungen über das Ikosaeder"; Du-Val 1964 "Homographies,
//! Quaternions, and Rotations" §8.) The same `R_5` is one of the four
//! `H_4` Coxeter generators acting on the simple roots — the others
//! are the 2-fold and 3-fold reflections that already overlap with
//! the Z/3 × Z/3 generators of the Schoen quotient.
//!
//! ## Action on Schoen monomials
//!
//! With the diagonal `R_5` realisation on both `(x_*)` and `(y_*)`
//! blocks (the Klein convention), the action on the Schoen
//! monomial-exponent tuple
//! `[a_0, a_1, a_2, b_0, b_1, b_2, c_0, c_1]` (encoding
//! `∏_k x_k^{a_k} ∏_k y_k^{b_k} t_0^{c_0} t_1^{c_1}`) is
//!
//! ```text
//!     χ_5(m) = (a_1 + 2 a_2 + b_1 + 2 b_2)  mod 5.
//! ```
//!
//! Note that this is **structurally the same formula** as the Z/3×Z/3
//! α-character, but reduced mod 5 instead of mod 3. The `t`-block
//! does not enter (the icosahedral fivefold acts as the identity on
//! the elliptic-fibre `(t_0:t_1)` direction in the Klein/Schoen
//! convention; one could instead take a fivefold rotation in `t` but
//! that would not commute with the Z/3 acting on `t_1` from
//! [`crate::route34::z3xz3_projector::beta_character`]).
//!
//! Combining with the existing Z/3 × Z/3 projection, the gateway
//! sector is the **(α, β, χ_5) = (0, 0, 0)** subset of basis monomials,
//! i.e. monomials simultaneously invariant under both the Schoen
//! Z/3 × Z/3 and the icosahedral Z/5 actions.
//!
//! ## Caveat
//!
//! This is the minimal **Z/5** of the full H_4 group — the
//! icosahedral fivefold rotation only. The complete H_4 Coxeter
//! action would also project on the 2-fold and 3-fold reflections;
//! those, however, partially overlap with the Z/3 × Z/3 generators
//! already projected against in [`crate::route34::z3xz3_projector`],
//! so the additional projection power gained from including them is
//! limited. The Z/5 projection alone is already a 1/5 reduction in
//! basis size (1/45 combined with Z/3 × Z/3 if independent), enough
//! to test whether ω_fix lives in this sector.
//!
//! ## Public surface
//!
//! Mirrors [`metric_laplacian_projected`]:
//!
//!   * [`icosa_z5_character`] — fivefold-character of a Schoen monomial.
//!   * [`build_h4_projected_basis_schoen`] — Schoen basis filter for
//!     `(α, β, χ_5) = (0, 0, 0)`.
//!   * [`compute_h4_projected_metric_laplacian_spectrum`] — Galerkin
//!     solve on the H_4-projected basis.
//!
//! ## P7.5 consumer
//!
//! [`crate::bin::p7_5_h4_gateway_test`] (binary) compares the lowest
//! H_4-projected eigenvalue to the ω_fix prediction.
//!
//! ## References
//!
//! * Coxeter, *Regular Polytopes*, 3rd ed. (Dover 1973), Tab. I (H_4
//!   Coxeter graph and exponents).
//! * Slansky, "Group Theory for Unified Model Building", *Phys. Rep.*
//!   **79** (1981) 1–128, §6 (E_8 root system and sub-Coxeter
//!   structure).
//! * Conway, Sloane, *Sphere Packings, Lattices and Groups*, 3rd ed.
//!   (Springer 1999), Ch. 4 §8.2 (icosian embedding into E_8).
//! * Wilson, R. A., *The Finite Simple Groups* (Springer 2009),
//!   §5.6.2 (icosahedral subgroup of E_8(2)).
//! * Klein, F., *Vorlesungen über das Ikosaeder* (Teubner 1884) —
//!   `Z_5` weights on `(x_0:x_1:x_2)`.
//! * Du Val, P., *Homographies, Quaternions, and Rotations*
//!   (Oxford 1964), §8 (binary icosahedral Z/5).

