/// Result type — matches [`crate::route34::metric_laplacian_projected::ProjectedSpectrumReport`]
/// with one extra field for the icosahedral-Z/5 survival fraction.
#[derive(Clone, Debug, Default, Serialize, Deserialize)]
pub struct H4ProjectedSpectrumReport {
    /// Standard Galerkin spectrum on the H_4-projected basis.
    pub spectrum: MetricLaplacianSpectrum,
    /// Symmetry projection that was applied (string label).
    pub projection: String,
    /// Original (full) basis dimension before any projection.
    pub full_basis_dim: usize,
    /// After Z/3 × Z/3 (or TY/Z3) trivial-rep filter only.
    pub gamma_projected_basis_dim: usize,
    /// After both the Γ-quotient and icosahedral-Z/5 filter.
    pub h4_projected_basis_dim: usize,
    /// `gamma_projected_basis_dim / full_basis_dim`.
    pub gamma_survival_fraction: f64,
    /// `h4_projected_basis_dim / full_basis_dim`.
    pub h4_survival_fraction: f64,
    /// `h4_projected_basis_dim / gamma_projected_basis_dim` —
    /// fraction of Γ-invariants that are also Z/5-invariant.
    pub h4_relative_survival_fraction: f64,
}

/// Compute the metric-Laplacian spectrum on the
/// `(α, β, χ_5) = (0, 0, 0)` (Schoen) or `(α, χ_5) = (0, 0)` (TY)
/// sub-Coxeter sector — the H_4 lepton sector of the journal
/// §L.1 / §L.2 identification.
pub fn compute_h4_projected_metric_laplacian_spectrum(
    metric: &dyn MetricBackground,
    config: &MetricLaplacianConfig,
    projection: ProjectionKind,
) -> H4ProjectedSpectrumReport {
    let started = Instant::now();
    let full = build_test_basis(config.max_total_degree);
    let full_basis_dim = full.len();

    // Step 1: project to Γ-invariants only (matches the existing
    // Z/3×Z/3 / Z/3 trivial-rep filter for direct comparability with
    // P7.2b).
    let gamma_basis: Vec<TestMonomial> = match projection {
        ProjectionKind::SchoenZ3xZ3 => full
            .iter()
            .filter(|m| alpha_character(&m.exponents) == 0 && beta_character(&m.exponents) == 0)
            .cloned()
            .collect(),
        ProjectionKind::TianYauZ3 => full
            .iter()
            .filter(|m| ty_z3_character(&m.exponents) == 0)
            .cloned()
            .collect(),
    };
    let gamma_dim = gamma_basis.len();

    // Step 2: additionally project on the icosahedral fivefold.
    let h4_basis: Vec<TestMonomial> = match projection {
        ProjectionKind::SchoenZ3xZ3 => gamma_basis
            .iter()
            .filter(|m| icosa_z5_character(&m.exponents) == 0)
            .cloned()
            .collect(),
        ProjectionKind::TianYauZ3 => gamma_basis
            .iter()
            .filter(|m| icosa_z5_character_ty(&m.exponents) == 0)
            .cloned()
            .collect(),
    };
    let n_b = h4_basis.len();
    let n_pts = metric.n_points();

    let proj_label = match projection {
        ProjectionKind::SchoenZ3xZ3 => "schoen_z3xz3_intersect_icosa_z5",
        ProjectionKind::TianYauZ3 => "ty_z3_intersect_icosa_z5",
    };

    let gamma_survival = if full_basis_dim > 0 {
        gamma_dim as f64 / full_basis_dim as f64
    } else {
        0.0
    };
    let h4_survival = if full_basis_dim > 0 {
        n_b as f64 / full_basis_dim as f64
    } else {
        0.0
    };
    let h4_rel_survival = if gamma_dim > 0 {
        n_b as f64 / gamma_dim as f64
    } else {
        0.0
    };

    if n_b == 0 || n_pts == 0 {
        return H4ProjectedSpectrumReport {
            spectrum: MetricLaplacianSpectrum {
                eigenvalues: Vec::new(),
                eigenvalues_full: Vec::new(),
                basis_dim: n_b,
                n_points: n_pts,
                wall_clock_seconds: started.elapsed().as_secs_f64(),
                basis_exponents: None,
                eigenvectors_full: None,
                orthogonalized_basis_dim: 0,
            },
            projection: proj_label.to_string(),
            full_basis_dim,
            gamma_projected_basis_dim: gamma_dim,
            h4_projected_basis_dim: n_b,
            gamma_survival_fraction: gamma_survival,
            h4_survival_fraction: h4_survival,
            h4_relative_survival_fraction: h4_rel_survival,
        };
    }

    let pts = metric.sample_points();

    // Evaluate the H_4-projected basis at every sample point + 8 partials.
    // Layouts mirror metric_laplacian_projected exactly.
    let mut basis_at = vec![Complex64::new(0.0, 0.0); n_b * n_pts];
    let mut d_basis_at = vec![Complex64::new(0.0, 0.0); n_b * 8 * n_pts];
    for alpha in 0..n_b {
        let exps = h4_basis[alpha].exponents;
        for p in 0..n_pts {
            basis_at[alpha * n_pts + p] = eval_monomial(&pts[p], &exps);
        }
        for i in 0..8 {
            let (coeff, dexps) = d_dz(&exps, i);
            if coeff == 0.0 {
                continue;
            }
            let cz = Complex64::new(coeff, 0.0);
            for p in 0..n_pts {
                d_basis_at[(alpha * 8 + i) * n_pts + p] = cz * eval_monomial(&pts[p], &dexps);
            }
        }
    }

    let m_mat = build_mass_matrix(&basis_at, n_b, metric);
    let k_mat = build_stiffness_matrix(&d_basis_at, n_b, metric);

    let m_inv = invert_hermitian_regularised(&m_mat, n_b, config.mass_regularisation);
    let l_eff = matmul_complex(&m_inv, &k_mat, n_b);

    let mut l_h = l_eff;
    for i in 0..n_b {
        let z = l_h[i * n_b + i];
        l_h[i * n_b + i] = Complex64::new(z.re, 0.0);
        for j in (i + 1)..n_b {
            let a = l_h[i * n_b + j];
            let b = l_h[j * n_b + i].conj();
            let avg = (a + b) * 0.5;
            l_h[i * n_b + j] = avg;
            l_h[j * n_b + i] = avg.conj();
        }
    }
    let (eigvals, evecs) =
        hermitian_jacobi_n(&l_h, n_b, config.jacobi_max_sweeps, config.jacobi_tol);

    let mut full_indexed: Vec<(usize, f64)> = eigvals
        .iter()
        .enumerate()
        .map(|(i, &v)| (i, v.abs()))
        .collect();
    full_indexed.sort_by(|a, b| a.1.partial_cmp(&b.1).unwrap_or(std::cmp::Ordering::Equal));
    let full_eigs: Vec<f64> = full_indexed.iter().map(|(_, v)| *v).collect();

    let n_low = config.n_low_eigenvalues.min(full_eigs.len());
    let low: Vec<f64> = full_eigs.iter().take(n_low).cloned().collect();

    let (basis_exponents, eigenvectors_full) = if config.return_eigenvectors {
        let mut sorted_evecs = vec![(0.0_f64, 0.0_f64); n_b * n_b];
        for (new_j, (old_j, _)) in full_indexed.iter().enumerate() {
            for i in 0..n_b {
                let z = evecs[i * n_b + *old_j];
                sorted_evecs[i * n_b + new_j] = (z.re, z.im);
            }
        }
        let exps: Vec<[u32; 8]> = h4_basis.iter().map(|m| m.exponents).collect();
        (Some(exps), Some(sorted_evecs))
    } else {
        (None, None)
    };

    H4ProjectedSpectrumReport {
        spectrum: MetricLaplacianSpectrum {
            eigenvalues: low,
            eigenvalues_full: full_eigs,
            basis_dim: n_b,
            n_points: n_pts,
            wall_clock_seconds: started.elapsed().as_secs_f64(),
            basis_exponents,
            eigenvectors_full,
            orthogonalized_basis_dim: 0,
        },
        projection: proj_label.to_string(),
        full_basis_dim,
        gamma_projected_basis_dim: gamma_dim,
        h4_projected_basis_dim: n_b,
        gamma_survival_fraction: gamma_survival,
        h4_survival_fraction: h4_survival,
        h4_relative_survival_fraction: h4_rel_survival,
    }
}

